"""Eval scorer — uses LLM as judge to score documentation quality."""

from dataclasses import dataclass
import json
from openai import AsyncOpenAI

from core.mimo_client import mimo_chat, ClientStats, MIMO_MODEL
from core.scanner import ScanResult
from core.agent import AgentOutput


@dataclass
class QualityScore:
    """Quality evaluation result for generated documentation."""
    overall: int  # 0-100
    completeness: int  # 0-100
    accuracy: int  # 0-100
    clarity: int  # 0-100
    structure: int  # 0-100
    usefulness: int  # 0-100
    judge_reasoning: str
    recommendations: list[str]


JUDGE_SYSTEM = """You are an impartial documentation quality judge for DocsForge.

You will receive:
1. A codebase scan summary
2. Documentation generated by 5 specialist agents
3. The synthesized final documentation

Score the documentation on these dimensions (0-100 each):
- completeness: Does it cover architecture, APIs, examples, config, changelog?
- accuracy: Does it reflect the actual codebase structure and contents?
- clarity: Is it well-written and easy to understand?
- structure: Is it well-organized with proper navigation?
- usefulness: Would a new developer find it actionable?

Be strict. 80+ means excellent. 50 is average. Below 30 is poor.

Return ONLY valid JSON:
{
  "overall": N,
  "completeness": N,
  "accuracy": N,
  "clarity": N,
  "structure": N,
  "usefulness": N,
  "reasoning": "brief explanation",
  "recommendations": ["rec1", "rec2"]
}"""


class DocScorer:
    """Evaluates documentation quality using LLM as judge."""

    def __init__(self, client: AsyncOpenAI, model: str = MIMO_MODEL, stats: ClientStats | None = None):
        self.client = client
        self.model = model
        self.stats = stats or ClientStats()

    async def evaluate(
        self,
        scan_result: ScanResult,
        agent_outputs: dict[str, AgentOutput],
        final_pages: dict[str, str],
    ) -> QualityScore:
        """Score the generated documentation."""
        agent_summary = ""
        for name, output in agent_outputs.items():
            agent_summary += f"\n### {name}\n"
            for title, content in output.sections.items():
                agent_summary += f"**{title}:** {content[:500]}\n"

        pages_summary = ""
        for filename, content in final_pages.items():
            pages_summary += f"\n### {filename}\n{content[:1000]}\n"

        user_msg = f"""## Codebase Summary
- Root: {scan_result.root}
- Files: {scan_result.total_files}
- Languages: {scan_result.languages}

## Agent Outputs
{agent_summary}

## Final Documentation Pages
{pages_summary}

Score this documentation and return JSON:"""

        raw = await mimo_chat(
            self.client,
            self.model,
            messages=[
                {"role": "system", "content": JUDGE_SYSTEM},
                {"role": "user", "content": user_msg},
            ],
            temperature=0.1,
            max_tokens=1024,
            stats=self.stats,
        )

        return self._parse(raw)

    def _parse(self, raw: str) -> QualityScore:
        text = raw.strip()
        if "```" in text:
            parts = text.split("```")
            if len(parts) >= 2:
                text = parts[1]
                if text.startswith("json"):
                    text = text[4:]

        try:
            data = json.loads(text.strip())
        except json.JSONDecodeError:
            return QualityScore(
                overall=50, completeness=50, accuracy=50,
                clarity=50, structure=50, usefulness=50,
                judge_reasoning=f"Failed to parse: {raw[:300]}",
                recommendations=["Re-run evaluation"],
            )

        return QualityScore(
            overall=data.get("overall", 50),
            completeness=data.get("completeness", 50),
            accuracy=data.get("accuracy", 50),
            clarity=data.get("clarity", 50),
            structure=data.get("structure", 50),
            usefulness=data.get("usefulness", 50),
            judge_reasoning=data.get("reasoning", ""),
            recommendations=data.get("recommendations", []),
        )
